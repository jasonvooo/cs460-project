      <p>
       The Latin letter 'E' differs little from its source, the
       <a href="/wiki/Greek_alphabet" title="Greek alphabet">
        Greek
       </a>
       letter
       <a href="/wiki/Epsilon" title="Epsilon">
        epsilon
       </a>
       , ''. This in turn comes from the
       <a class="mw-redirect" href="/wiki/Semitic_alphabet" title="Semitic alphabet">
        Semitic
       </a>
       letter
       <i>
        <a href="/wiki/He_(letter)" title="He (letter)">
         h
        </a>
       </i>
       , which has been suggested to have started as a praying or calling human figure (
       <i>
        <a class="new" href="/w/index.php?title=Hillul&amp;action=edit&amp;redlink=1" title="Hillul (page does not exist)">
         hillul
        </a>
       </i>
       'jubilation'), and was probably based on a similar
       <a class="mw-redirect" href="/wiki/Egyptian_hieroglyph" title="Egyptian hieroglyph">
        Egyptian hieroglyph
       </a>
       that indicated a different pronunciation. In Semitic, the letter represented
       <span class="IPA" title="Representation in the International Phonetic Alphabet (IPA)">
        /h/
       </span>
       (and
       <span class="IPA" title="Representation in the International Phonetic Alphabet (IPA)">
        /e/
       </span>
       in foreign words); in Greek,
       <i>
        h
       </i>
       became the letter epsilon, used to represent
       <span class="IPA" title="Representation in the International Phonetic Alphabet (IPA)">
        /e/
       </span>
       .  The various forms of the
       <a href="/wiki/Old_Italic_script" title="Old Italic script">
        Old Italic script
       </a>
       and the
       <a href="/wiki/Latin_alphabet" title="Latin alphabet">
        Latin alphabet
       </a>
       followed this usage.
      </p>
